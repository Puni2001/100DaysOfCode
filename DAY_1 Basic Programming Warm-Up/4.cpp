// checking given number is Odd or even

#include<bits/stdc++.h>
using namespace std;
int main(){
    // Taking input from user 
    int n;
    cin>>n;

    // checking the given number is odd or even 
    if ( n % 2 == 0){
        cout<<"Given Number "<<n<<" is Even"<<endl;
    }
    else {
        cout<<"Given Number "<<n<<" is Odd"<<endl;
    }

}